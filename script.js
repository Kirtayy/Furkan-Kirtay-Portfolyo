const form = document.getElementById('contactForm');
const formNote = document.getElementById('formNote');
const submitText = document.getElementById('submitText');

form.addEventListener('submit', (e) => {
  e.preventDefault();

  const name = form.name.value.trim();
  const email = form.email.value.trim();
  const subject = form.subject.value.trim();
  const message = form.message.value.trim();
  const msgs = formMessages[currentLang];

  if (!name || !email || !subject || !message) {
    formNote.textContent = msgs.incomplete;
    formNote.className = 'form-note error';
    return;
  }

  submitText.textContent = msgs.preparing;

  const body = `${name} (${email})%0D%0A%0D%0A${encodeURIComponent(message)}`;
  const mailto = `mailto:furkankirtay1470@gmail.com?subject=${encodeURIComponent(subject)}&body=${body}`;

  window.location.href = mailto;

  formNote.textContent = msgs.opened;
  formNote.className = 'form-note success';
  submitText.textContent = translations['form.submit'][currentLang];

  setTimeout(() => {
    form.reset();
  }, 800);
});

